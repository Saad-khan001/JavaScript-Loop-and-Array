// Create a program that calculates the sum of all numbers in an array.



// [10, 20, 30, 40, 50]



// Sum = 150


// Arrays
let numbers = [10, 20, 30, 40, 50];

// variable to store the sum
let sum = 0;

// for loop function

for (let i = 0; i <= 4; i++) {
    console.log(numbers[i]);
    sum = sum + numbers[i];
}

console.log("Sum =", sum);